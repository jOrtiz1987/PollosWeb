package com.mx.pollos.model;

public class VentaSemana {

	private Integer idSemana;
	private Integer gastos;
	private Integer sueldos;
	private Integer productos;
	private Integer ingresos;
	private Integer ganancia;
	
	public Integer getIdSemana() {
		return idSemana;
	}
	public void setIdSemana(Integer idSemana) {
		this.idSemana = idSemana;
	}
	public Integer getGastos() {
		return gastos;
	}
	public void setGastos(Integer gastos) {
		this.gastos = gastos;
	}
	public Integer getSueldos() {
		return sueldos;
	}
	public void setSueldos(Integer sueldos) {
		this.sueldos = sueldos;
	}
	public Integer getProductos() {
		return productos;
	}
	public void setProductos(Integer productos) {
		this.productos = productos;
	}
	public Integer getIngresos() {
		return ingresos;
	}
	public void setIngresos(Integer ingresos) {
		this.ingresos = ingresos;
	}
	public Integer getGanancia() {
		return ganancia;
	}
	public void setGanancia(Integer ganancia) {
		this.ganancia = ganancia;
	}
	
}
